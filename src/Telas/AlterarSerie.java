    public void pesquisar() {
        String dados[][] = c_Serie.Pesquisar().clone();
        for (int i = 0; i < dados.length; i++) {
            modelo.addRow(new String[]{dados[i][0], dados[i][1], dados[i][2], dados[i][3], dados[i][4]});
        }
    }

    public void limparTabela() {
        for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

