    private void bt_deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_deletarActionPerformed
        c_Serie.Deletar(Integer.parseInt(tf_id.getText()));
        JOptionPane.showMessageDialog(null, "Deletado com sucesso.");
        limparTabela();
        tf_nome.setText("");
        tf_genero.setText("");
        tf_temporadas.setText("");
        cb_assistido.setSelected(false);
        pesquisar();
    }//GEN-LAST:event_bt_deletarActionPerformed

    private void tb_serieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_serieMouseClicked
       bt_deletar.setEnabled(true);
        bt_limpar.setEnabled(true);
        bt_salvar.setEnabled(true);

        int linha = tb_serie.getSelectedRow();

        tf_id.setText((String) (modelo.getValueAt(linha, 0)));
        tf_nome.setText((String) modelo.getValueAt(linha, 1));
        tf_genero.setText((String) modelo.getValueAt(linha, 2));
        tf_temporadas.setText((String) modelo.getValueAt(linha, 3));
        cb_assistido.setSelected((Boolean) modelo.getValueAt(linha, 4));
    }//GEN-LAST:event_tb_serieMouseClicked


