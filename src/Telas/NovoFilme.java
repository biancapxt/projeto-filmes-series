    private void bt_salvarnovofilmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarnovofilmeActionPerformed

        String nome = tf_nome.getText();
        String genero = tf_genero.getText();
        int ano = Integer.parseInt(tf_ano.getText());
        assistido = 0;
        
        if (cb_assistido.isSelected()) {
            assistido = 1;
        }

        c_Filme.Salvar(nome, genero, ano, assistido);
        JOptionPane.showMessageDialog(null, "Salvo com sucesso");

        bt_salvarnovofilme.setEnabled(false);

    }//GEN-LAST:event_bt_salvarnovofilmeActionPerformed


